                            <div class="col-md-3 label ">Title</div>
                            <div class="col-md-9 fw-bold">IMPLEMENTASI METODE WEIGHTED PRODUCT PADA SISTEM PENDUKUNG KEPUTUSAN PENERIMA BEASISWA BIDIKMISI UNIVERSITAS MATARAM</div>
                        </div>
                        <div class="row">
                            <div class="col-md-3 label ">DOI</div>
                            <div class="col-md-9"><a href="https://doi.org/10.29303/jtika.v3i1.108" target="_blank">https://doi.org/10.29303/jtika.v3i1.108</a></div>
                        </div>
                        <div class="row">
                            <div class="col-md-3 label ">Keywords</div>
                            <div class="col-md-9">Weighted Product, Bidikmisi Scholarship, Decision Support System, Codeigniter Framework, mysql, PHP</div>
                        </div>
                        <div class="row">
                            <div class="col-md-3 label ">Abstract</div>
                            <div class="col-md-9">Pemberian beasiswa merupakan program kerja yang ada di setiap Universitas atau perguruan tinggi. Bidikmisi adalah program bantuan biaya pendidikan yang diberikan pemerintah melalui Direktorat Jenderal Pendidikan Tinggi. Dalam melakukan penetapan mahasiswa peserta beasiswa bidikmisi pihak Universitas masih menggunakan cara manual yaitu dengan cara melakukan pengecekan kembali data mahasiswa satu persatu di Microsoft excel oleh Subbagian Kesejahteraan Mahasiswa dan hasil seleksi data diurutkan berdasarkan fakultas atau prodi masing-masing. Untuk membantu dalam proses seleksi maka perlu dibangun sistem pendukung keputusan, agar dapat menjadi pertimbangan dalam mengambil keputusan. Dalam sistem pendukung keputusan ini menggunakan 10 kriteria antara lain: penghasilan ayah, penghasilan ibu, jumlah tanggungan, kepemilikan rumah, luas tanah, luas bangunan, jarak pusat kota, mandi cuci kakus, sumber listrik dan sumber air. Didalam sistem pendukung keputusan ini perhitungannya menggunakan metode weighted product. Metode weighted product digunakan karena metode ini lebih bisa memberikan nilai bobot disetiap kriteria kemudian dilakukan perangkingan dari yang terbesar ke terkecil dari hasil perhitungan nilai akhir. Dalam pembangunan sistem ini, menggunakan bahasa pemograman PHP dan database yang digunakan mysql dengan menggunakan framework Codeigniter.</div>
